package Project_C_C_Convertor;

public class currency_rates
{
double btc_to_usd(double btc)
{
	return btc*35603;
}
double usd_to_btc(double usd)
{
	return usd/35603;
}

double btc_to_pkr(double btc)
{
	return btc*5720800.41;
}
double pkr_to_btc(double pkr)
{
	return pkr/5720800.41;
}

double eth_to_usd(double eth)
{
	return eth*1098.94;
}
double usd_to_eth(double usd)
{
	return usd/1098.94;
}

double eth_to_pkr(double eth)
{
	return eth*176219.55;
}
double pkr_to_eth(double pkr)
{
	return pkr/176219.55;
}

double btc_to_eth(double btc)
{
	return btc*32.46;
}

double eth_to_btc(double eth)
{
	return eth/32.46;
}

	
}
